<p id="sae" class="titreparagraphe"> Situation d'Apprentissage et d'Évaluation </p>

<div class="paragraphe3">

  <p class="paragraphe3"> Dans l’optique de préparer au mieux les étudiants à leur future vie professionnelle, les étudiants sont regroupés en agences de communication 
    de 3 à 7 personnes dans des projets appelés "SAÉ" ou Situation d'Apprentissage et d'Évaluation. Ces agences ont pour but d’encourager le travail
    d’équipe dans un cadre reprenant l'environnement du travail en entreprise.</p>

  <p class="paragraphe3"> La situation d'apprentissage et d'évaluation ou simplement SAÉ est la situation didactique que privilégie, au Québec, le Ministère de l'Éducation, Enseignement supérieur et Recherche (MEESR) par le biais du
    Programme de formation de l'école québécoise (PFEQ). Une SAÉ se définit comme un ensemble constitué d’une ou plusieurs tâches à réaliser par 
    l’élève en vue d’atteindre le but fixé. Elle permet : à l’élève, de développer et d’exercer une ou plusieurs compétences disciplinaires et
    transversales; à l’enseignant, d’assurer le suivi du développement des compétences dans une perspective d’aide à l’apprentissage.
    Elle est donc centrée sur l'élève et préconise une approche constructiviste ou socioconstructiviste à l'école.</p>

  <p class="paragraphe3">Les SAÉ sont une nouveauté des diplômes BUT,<span class="span">elles visent à remplacer les Devoirs Sur Table et les notes à terme </span>,
    en proposant une approche plus
   "ingénieure" de la scolarité avec des étudiants qui apprennent à résoudre des problèmes et non plus apprendre par cœur leurs cours.</p>
</div>


